public class BooleanLiterals {
    public static void main(String[] args) {
        // Boolean literals
        boolean condition1 = true;
        boolean condition2 = false;

        // Using boolean literals in if-else statements
        if (condition1) {
            System.out.println("Condition 1 is true, so this block will execute.");
        } else {
            System.out.println("Condition 1 is false, so this block will not execute.");
        }

        if (condition2) {
            System.out.println("Condition 2 is true, so this block will not execute.");
        } else {
            System.out.println("Condition 2 is false, so this block will execute.");
        }
    }
}
