public class Switch26 { 
    public static void main(String[] args) { 
        int number = 5; 
        switch(number) { 
            case 5: 
                System.out.println("Number is 5"); 
                break; 
            // You can remove the second case 5 or replace it with a different value
            case 6: 
                System.out.println("This is case 6");
                break; 
            default: 
                System.out.println("This is the default case"); 
        } 
    } 
}
